#!/usr/bin/env node
var Contacts = require( '..' )
var argv = require( 'yargs' )
  .usage( '\nUsage: contacts <command> [options]' )
  .command( '', '' )
  .command( 'help <term>', 'View detailed help for given command or term' )
  .command( '', '' )
  .command( 'search <term>', 'Search contacts' )
  .command( 'show <name>', 'Show details of a contact' )
  .command( '', '' )
  .command( 'import [options]', 'Import contact data from files or a directory' )
  .command( 'export [options]', 'Export contact data' )
  .command( '', '' )
  .demand( 1 )
  .help( 'help' )
  .alias( 'help', 'h' )
  .count( 'verbose' )
  .alias( 'verbose', 'v' )
  .describe( 'verbose', 'Enable verbose output' )
  // .locale( 'pirate' )
  .locale( 'en' )
  .argv

console.log( argv )
